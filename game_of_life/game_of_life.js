function draw() {
	let size = canvas.height / 25;
	for (let i = 0; i < 25; ++i) {
		for (let j = 0; j < 25; ++j) {
			let x = size * i;
			let y = size * j;
			let number = grid[i][j];
			if (number == 0) {
				context.fillRect(x, y, size, size);
			} else {
				let temp = context.fillStyle;
				context.fillStyle = "yellow";
				context.fillRect(x, y, size, size);
				context.fillStyle = temp;
			}
		}
	}
}

